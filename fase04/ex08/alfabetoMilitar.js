    
function alfabetoMilitar(vogal){
    if (vogal === 'a'||vogal === 'A')
    return 'alpha' 
    if (vogal === 'e'||vogal === 'E')
    return 'echo'
    if (vogal === 'i'||vogal === 'I')
    return 'india'
    if (vogal === 'o'||vogal === 'O')
    return 'oscar'
    if (vogal ==='u'||vogal === 'U')
    return 'uniform'
    else
    return false
}



